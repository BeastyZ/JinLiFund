package utils;
/*
 * 基金经理实体类
 */
public class FundManager {

	private int id;
	private String manName;
	private String manWorkFor;
	private String manOnTime;
	private String manPresentFund;
	private double manAAR;
	private String manGender;
	private String manEB;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getManName() {
		return manName;
	}
	public void setManName(String manName) {
		this.manName = manName;
	}
	public String getManWorkFor() {
		return manWorkFor;
	}
	public void setManWorkFor(String manWorkFor) {
		this.manWorkFor = manWorkFor;
	}
	public String getManOnTime() {
		return manOnTime;
	}
	public void setManOnTime(String manOnTime) {
		this.manOnTime = manOnTime;
	}
	public String getManPresentFund() {
		return manPresentFund;
	}
	public void setManPresentFund(String manPresentFund) {
		this.manPresentFund = manPresentFund;
	}
	public double getManAAR() {
		return manAAR;
	}
	public void setManAAR(double manAAR) {
		this.manAAR = manAAR;
	}
	public String getManGender() {
		return manGender;
	}
	public void setManGender(String manGender) {
		this.manGender = manGender;
	}
	public String getManEB() {
		return manEB;
	}
	public void setManEB(String manEB) {
		this.manEB = manEB;
	}
}
